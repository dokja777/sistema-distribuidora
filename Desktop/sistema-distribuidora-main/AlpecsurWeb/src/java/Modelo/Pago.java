public class Pago {
    private int idPago;
    private double monto;

    public Pago() {
    }

    
    public Pago(int idPago, double monto) {
        this.idPago = idPago;
        this.monto = monto;
    }

    public int getIdPago() {
        return idPago;
    }

    public void setIdPago(int idPago) {
        this.idPago = idPago;
    }

    public double getMonto() {
        return monto;
    }

    public void setMonto(double monto) {
        this.monto = monto;
    }
    
    
    
    
}
